package day2.methods;

public class Example2 {
	public static void add() {
		int x = 5;
		int y = 4;
		int z = x + y;
		System.out.println(z);
	}
	
	public static void subtract() {
		int x = 14;
		int y = 8;
		int z = x - y;
		System.out.println(z);
	}
	
	
	public static void main(String[] args) {
		add();
		subtract();
	}
}
