package day1;

public class Example8 {

	public static void main(String[] args) {
		double myDouble = 9.78;
		int myInt = (int) myDouble; //Automatic Casting
		
		System.out.println(myDouble);
		System.out.println(myInt);
		
		
		System.out.println(myInt++);
		System.out.println(++myInt);
		

	}

}
